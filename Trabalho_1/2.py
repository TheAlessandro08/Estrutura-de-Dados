# Lista para armazenar os números
numeros = [15, 20, 5, 30, 10]

# Variável acumuladora
soma_total = 0

# Laço for para percorrer a lista
for numero in numeros:
    soma_total += numero  # Soma o valor atual ao acumulador

# Resultado final na tela
print(f"A soma de todos os elementos da lista é: {soma_total}")